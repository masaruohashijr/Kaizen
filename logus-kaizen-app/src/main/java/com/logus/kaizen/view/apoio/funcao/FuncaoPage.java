package com.logus.kaizen.view.apoio.funcao;

import java.util.Collection;

import com.logus.core.model.dialog.DialogButtonType;
import com.logus.core.model.translation.CoreTranslator;
import com.logus.core.model.translation.TM;
import com.logus.core.view.form.BeanForm;
import com.logus.core.view.list.BeanGrid;
import com.logus.kaizen.app.KaizenAbstractPage;
import com.logus.kaizen.app.KaizenMainLayout;
import com.logus.kaizen.model.apoio.ApoioDataService;
import com.logus.kaizen.model.apoio.funcao.Funcao;
import com.logus.kaizen.model.translation.KaizenTranslator;
import com.vaadin.flow.router.Route;

/**
 *
 * @author Masaru Ohashi Júnior
 * @since 1 de mar de 2019
 * @version 1.0.0
 *
 */
@Route(value = "funcao", layout = KaizenMainLayout.class)
public class FuncaoPage extends KaizenAbstractPage<Funcao> {

	public FuncaoPage() {
		super(DialogButtonType.SAVE);
		setEditWindowWidth(1000);
		setEditWindowHeight(800);
	}

	/**
	 *
	 */
	private static final long serialVersionUID = -99267169911263536L;

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.ListViewPage#loadAll()
	 */
	@Override
	protected Collection<Funcao> loadAll() {
		return ApoioDataService.get().getFuncaoDao().loadAll();
	}

	/**
	 * Método responsável por criar a tabela contendo todos as funcoes persistidas
	 * no banco
	 *
	 * @return BeanGrid<Funcao>
	 */
	@Override
	protected BeanGrid<Funcao> createGrid() {
		return new FuncaoGrid();
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#createForm(java.lang.Object)
	 */
	@Override
	protected BeanForm<Funcao> createForm(Funcao funcao) {
		return new FuncaoForm(funcao);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#deleteObject(java.lang.Object)
	 */
	@Override
	protected void deleteObject(Funcao funcao) throws Exception {
		ApoioDataService.get().getFuncaoDao().delete(funcao);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#insertObject(java.lang.Object)
	 */
	@Override
	protected void insertObject(Funcao funcao) throws Exception {
		ApoioDataService.get().getFuncaoDao().insert(funcao);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#updateObject(java.lang.Object)
	 */
	@Override
	protected void updateObject(Funcao funcao) throws Exception {
		ApoioDataService.get().getFuncaoDao().update(funcao);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getNomeEntidade()
	 */
	@Override
	protected Object getNomeEntidade() {
		return TM.translate(KaizenTranslator.FUNCAO);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getNomeEntidadePlural()
	 */
	@Override
	protected String getNomeEntidadePlural() {
		return TM.translate(KaizenTranslator.FUNCAO_PLURAL);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getArtigoEntidade()
	 */
	@Override
	protected Object getArtigoEntidade() {
		return TM.translate(CoreTranslator.ART_FEM_SING);
	}

}
