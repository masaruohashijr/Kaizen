package com.logus.kaizen.view.apoio.biblioteca;

import java.util.Collection;

import com.logus.core.model.dialog.DialogButtonType;
import com.logus.core.model.translation.CoreTranslator;
import com.logus.core.model.translation.TM;
import com.logus.core.view.form.BeanForm;
import com.logus.core.view.list.BeanGrid;
import com.logus.kaizen.app.KaizenAbstractPage;
import com.logus.kaizen.app.KaizenMainLayout;
import com.logus.kaizen.model.apoio.ApoioDataService;
import com.logus.kaizen.model.apoio.biblioteca.Biblioteca;
import com.logus.kaizen.model.translation.KaizenTranslator;
import com.vaadin.flow.router.Route;

/**
 *
 * @author Masaru Ohashi Júnior
 * @since 1 de mar de 2019
 * @version 1.0.0
 *
 */
@Route(value = "biblioteca", layout = KaizenMainLayout.class)
public class BibliotecaPage extends KaizenAbstractPage<Biblioteca> {

	public BibliotecaPage() {
		super(DialogButtonType.SAVE);
	}

	/**
	 *
	 */
	private static final long serialVersionUID = -99267169911263536L;

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.ListViewPage#loadAll()
	 */
	@Override
	protected Collection<Biblioteca> loadAll() {
		return ApoioDataService.get().getBibliotecaDao().loadAll();
	}

	/**
	 * Método responsável por criar a tabela contendo todas as bibliotecas
	 * persistidas no banco
	 *
	 * @return BeanGrid<Biblioteca>
	 */
	@Override
	protected BeanGrid<Biblioteca> createGrid() {
		return new BibliotecaGrid();
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#createForm(java.lang.Object)
	 */
	@Override
	protected BeanForm<Biblioteca> createForm(Biblioteca biblioteca) {
		return new BibliotecaForm(biblioteca);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#deleteObject(java.lang.Object)
	 */
	@Override
	protected void deleteObject(Biblioteca biblioteca) throws Exception {
		ApoioDataService.get().getBibliotecaDao().delete(biblioteca);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#insertObject(java.lang.Object)
	 */
	@Override
	protected void insertObject(Biblioteca biblioteca) throws Exception {
		ApoioDataService.get().getBibliotecaDao().insert(biblioteca);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see
	 * com.logus.core.view.list.AbstractListEditor#updateObject(java.lang.Object)
	 */
	@Override
	protected void updateObject(Biblioteca biblioteca) throws Exception {
		ApoioDataService.get().getBibliotecaDao().update(biblioteca);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getNomeEntidade()
	 */
	@Override
	protected Object getNomeEntidade() {
		return TM.translate(KaizenTranslator.BIBLIOTECA);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getNomeEntidadePlural()
	 */
	@Override
	protected String getNomeEntidadePlural() {
		return TM.translate(KaizenTranslator.BIBLIOTECA_PLURAL);
	}

	/*
	 * (non-Javadoc)
	 *
	 * @see com.logus.core.view.list.AbstractListEditor#getArtigoEntidade()
	 */
	@Override
	protected Object getArtigoEntidade() {
		return TM.translate(CoreTranslator.ART_MAS_SING);
	}

}
